#multiply table by while loop
print ("Multiplication table printing by while loop")
x = int(input("Enter number to print  \n"))  #Taking input
y = int(input("Enter number to print  \n")) 
i = 1
while i <=y:                      #Condition
     print (x ," X ", i, "=", x*i)              #Output
     i = i + 1                  #Increament